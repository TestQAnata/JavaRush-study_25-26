package JavaSyntax.ModuleOne.Theory_JR;

public class IfElse {
    public static void main(String[] args) {
        int age = 17;
        if (age < 18)
            System.out.println("Вы ещё ребёнок");
        else
            System.out.println("Вы уже взрослый");

        int temperature = 5;
        if (temperature < 0)
            System.out.println("На улице мороз");
        else
            System.out.println("Тепло");

        int age1 = 18;
        if (age1 == 18)
            System.out.println("Явитесь в военкомат");
        else
            System.out.println("Всё равно явитесь");

    }
}
